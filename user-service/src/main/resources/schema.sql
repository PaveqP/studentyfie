CREATE TABLE IF NOT EXISTS studentLearns (
    studentLearn_id INTEGER GENERATED BY DEFAULT AS IDENTITY NOT NULL PRIMARY KEY,
    university VARCHAR(255),
    course VARCHAR(100),
    course_name VARCHAR(255),
    program_id VARCHAR(100),
    rating FLOAT,
    exchange_programs_id INTEGER
);


CREATE TABLE IF NOT EXISTS students (
    student_id INTEGER GENERATED BY DEFAULT AS IDENTITY NOT NULL PRIMARY KEY,
    email VARCHAR(255) UNIQUE NOT NULL,
    about_me VARCHAR(7000),
    resume_file BYTEA,
    first_name VARCHAR(100) NOT NULL,
    surname VARCHAR(100) NOT NULL,
    last_name VARCHAR(100),
    birth_date DATE,
    learn_info_id INTEGER REFERENCES studentLearns(studentLearn_id) ON DELETE CASCADE,
    avatar BYTEA
);

CREATE TABLE IF NOT EXISTS studentSocials (
    socials_id INTEGER GENERATED BY DEFAULT AS IDENTITY NOT NULL PRIMARY KEY,
    name VARCHAR(100),
    link VARCHAR(255),
    students_id INTEGER REFERENCES students(student_id) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS agent (
    agent_id INTEGER GENERATED BY DEFAULT AS IDENTITY NOT NULL PRIMARY KEY,
    avatar VARCHAR(255),
    email VARCHAR(255) UNIQUE NOT NULL,
    first_name VARCHAR(100) NOT NULL,
    surname VARCHAR(100) NOT NULL,
    last_name VARCHAR(100),
    university_id INTEGER
);

CREATE TABLE IF NOT EXISTS agentContacts (
    agentContacts_id INTEGER GENERATED BY DEFAULT AS IDENTITY NOT NULL PRIMARY KEY,
    name VARCHAR(100),
    link VARCHAR(255),
    agent_id INTEGER REFERENCES agent(agent_id) ON DELETE CASCADE
);