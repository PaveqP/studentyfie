CREATE TABLE IF NOT EXISTS studentLearns (
    studentLearn_id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL PRIMARY KEY,
    university VARCHAR(255),
    course VARCHAR(100),
    course_name VARCHAR(255),
    program_id VARCHAR(100),
    rating FLOAT,
    exchange_programs_id INT
);


CREATE TABLE IF NOT EXISTS students (
    student_id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL PRIMARY KEY,
    email VARCHAR(255) UNIQUE NOT NULL,
    about_me VARCHAR(7000),
    resume_file BYTEA,
    first_name VARCHAR(100) NOT NULL,
    surname VARCHAR(100) NOT NULL,
    last_name VARCHAR(100),
    birth_date DATE,
    learn_info_id INT REFERENCES studentLearns(studentLearn_id) ON DELETE CASCADE,
    avatar BYTEA
);

CREATE TABLE IF NOT EXISTS studentSocials (
    socials_id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL PRIMARY KEY,
    name VARCHAR(100),
    link VARCHAR(255)
    students_id INT REFERENCES students(students_id) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS agent (
    agent_id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL PRIMARY KEY,
    avatar VARCHAR(255),
    email VARCHAR(255) UNIQUE NOT NULL,
    first_name VARCHAR(100) NOT NULL,
    surname VARCHAR(100) NOT NULL,
    last_name VARCHAR(100),
    university_id INT
    agentContacts_id INT REFERENCES agentContacts(agentContacts_id) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS agentContacts (
    agentContacts_id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL PRIMARY KEY,
    name VARCHAR(100),
    link VARCHAR(255)
);